/**
 * This class is responsible for handling Channel information
 * Created by cthammana on 08.09.2016.
 */
public interface Channel
{
	/**
	 * Gets id.
	 *
	 * @return the id
	 */
	Integer getId();

	/**
	 * Sets id.
	 *
	 * @param id the id
	 */
	void setId(Integer id);

	/**
	 * Gets name.
	 *
	 * @return the name
	 */
	String getName();

	/**
	 * Sets name.
	 *
	 * @param name the name
	 */
	void setName(String name);
}